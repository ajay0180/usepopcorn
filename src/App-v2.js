const average = (arr) =>
  arr.reduce((acc, cur, i, arr) => acc + cur / arr.length, 0);

const KEY = "51ffcf9c";

export default function App() {
  const [query, setQuery] = useState("");
  const [movies, setMovies] = useState([]);
  const [watched, setWatched] = useState([]);
  const [isLoading, setIsLoading] = useState(false);
  const [error, setError] = useState("");
  const [selectedId, setSelectedId] = useState(null);

  // const tempQuery = "interstellar";

  function handleSelectMovie(id) {
    setSelectedId((selectedId) => (id === selectedId ? null : id));
  }

  function handleCloseMovie() {
    setSelectedId(null);
  }

  function HandleWatch(movie) {
    setWatched((watched) => [...watched, movie]);
  }

  function HandleDeleteWatched(id) {
    setWatched((watched) => watched.filter((movie) => movie.imdbID !== id));
  }

  useEffect(
    //effect calback function is synchronous to avoid race condition. So,we made effect callback function as async function inside an arrow function.
    function () {
      //1. Declaring a variable for 'abort controller' Web API.
      const controller = new AbortController();

      async function fetchMovies() {
        try {
          setIsLoading(true);
          setError("");
          //  "Side-effect"  //
          const res = await fetch(
            `http://www.omdbapi.com/?apikey=${KEY}&s=${query}`,
            //2. This is the second arguments passed in 'fetch' API as an abort controller object.
            { signal: controller.signal }
          );

          //This happens when we are unable to send request due to network issue or some internal issue.
          if (!res.ok)
            throw new Error("Something went wrong with fetching movies! ");

          const data = await res.json();
          // console.log(data);

          //This happens when we send wrong query.
          if (data.Response === "False") throw new Error("Movie not found.");

          setMovies(data.Search);
          // console.log(data.Search);

          return () => console.log("cleanup");
        } catch (err) {
          if (err.name !== "AbortError") {
            setError(err.message);
          }
        } finally {
          setIsLoading(false);
        }

        if (query.length < 3) {
          setMovies([]);
          setError("");
          return;
        }
      }

      handleCloseMovie();
      fetchMovies();

      //'Cleanup' function
      return function () {
        controller.abort();
      };
    },
    [query]
  );

  return (
    <>
      <NavBar>
        <Logo />
        <Search query={query} setQuery={setQuery} />
        <NumResults movies={movies} />
      </NavBar>
      <Main movies={movies}>
        {/* To avoid "prop drilling" ,we use its better alternative method here, which is called,"Component Composition". */}
        <Box>
          {isLoading && <Loader />}
          {error && <ErrorMessage message={error} />}
          {!isLoading && !error && (
            <MovieList movies={movies} onSelectMovie={handleSelectMovie} />
          )}
        </Box>
        <Box>
          {selectedId ? (
            <MovieDetails
              selectedId={selectedId}
              onCloseMovie={handleCloseMovie}
              onAddWatch={HandleWatch}
              watchedMovie={watched}
            />
          ) : (
            <>
              <WatchedSummary watched={watched} />
              <WatchedMoviesList
                watched={watched}
                HandleDeleteWatched={HandleDeleteWatched}
              />
            </>
          )}
        </Box>

        {/* this is another alternative method to avoid  "prop drilling" ,and this is ,what we call as "passing element as prop"*/}
        {/* <Box element={<MovieList movies={movies} />} />
        <Box
          element={
            <>
              <WatchedSummary watched={watched} />
              <WatchedMoviesList watched={watched} />
            </>
          }
        /> */}
      </Main>
    </>
  );
}

